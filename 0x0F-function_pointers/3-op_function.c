#include "3-calc.h"

/**
 * op_add -	sums two integers.
 * @a:		first integer.
 * @b:		second integer.
 * Return:	the result.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -	subtracts two integers.
 * @a:		first integer.
 * @b:		second integer.
 * Return:	the result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul -	nultiplies two integers.
 * @a:		first integer.
 * @b:		second integer.
 * Return:	the result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -	divides two integers.
 * @a:		first integer.
 * @b:		second integer.
 * Return:	the result.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -	remainder of two integers.
 * @a:		first integer.
 * @b:		second integer.
 * Return:	the result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
